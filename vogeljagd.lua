versionNr="1.2.1"

-- Variables
starterStarted = false
started = false

shakeActive = false
shake = -50

score = 0
highscore = 0
ammo = 5
remainingTime = 60
gameOver = false
birds = {}
clouds = {[0]={x=30, y=20, movement=0.2, sprite=288}, [1]={x=75, y=42, movement=-0.35, sprite=289}, [2]={x=230, y=60, movement=0.15, sprite=290}, [3]={x=29, y=2, movement=0.32, sprite=291}}

t=0
tsec= 0

function ResetValues()
 starterStarted = false
 started = false

 shakeActive = false
	shake = -50
	
	score = 0
	highscore = 0
	ammo = 5
	remainingTime = 60
	gameOver = false
	birds = {}
	
	t=0
	tsec= 0
	
	init()
end

function MakeBird()
 mySprite = 257
	if math.random() < 0.5 then
	 mySprite = 257
	elseif math.random() < 0.7 then
	 mySprite = 260
	elseif math.random() < 0.6 then
	 mySprite = 263
	else
	 mySprite = 266
	end
	
 direction = 0
	if math.random() < 0.5 then
	 direction = 1
	else
	 direction = 2
	end
	
	layer = math.random()
	if layer < 0.3333 then
	 layer = 1
	elseif layer < 0.6666 then
	 layer = 2
	else
	 layer = 3
	end
	
	state = math.random(0, 2) - 1 
	
	x = 0
	if direction == 1 then
	 x = -20
	else
	 x = 240
	end
	
	height = 0
	if layer == 1 then
	 height = math.random(0, 70)
	elseif layer == 2 then
	 height = math.random(0, 80)
	else
	 height = math.random(0, 100)
	end
	
	speed = 0.0
	if layer == 1 then
	 speed = math.random(2, 10) / 10.0
	elseif layer == 2 then
	 speed = math.random(4, 13) / 10.0
	else
	 speed = math.random(7, 18) / 10.0
	end
	
	return {alive=true, birdSprite=mySprite, direction=direction, layer=layer, state=state, x=x, y=height, speed=speed, deadTime=0}
end

function init()
	-- Call math.random a few times to get better random numbers
 math.random()
	math.random()
	math.random()
	music()
	highscore = pmem(0)
	birds[0] = MakeBird()
end

init()

function TIC()
 if not started then
	 -- =====INPUT=====
		mx,my,md=mouse()
		
		-- =====MENU LOGIC=====
		for cloudNr = 0, #clouds do
		 clouds[cloudNr].x = clouds[cloudNr].x + clouds[cloudNr].movement
			if clouds[cloudNr].movement > 0 and clouds[cloudNr].x > 240 then
			 clouds[cloudNr].x = -20
			elseif clouds[cloudNr].movement < 0 and clouds[cloudNr].x < -20 then
			 clouds[cloudNr].x = 240
			end
		end
		
		if md then starterStarted = true end
		
		-- =====RENDER=====
		cls(12)
		map(0, 0, 30, 17, 0, 0, -1, 1)
		
		-- Show Clouds
		for cloudNr = 0, #clouds do
		 spr(clouds[cloudNr].sprite, clouds[cloudNr].x, clouds[cloudNr].y, 0, 2, 0, 0, 1, 1)
		end
		
		-- Show Trees
		spr(304, 223, 65, 0, 1, 0, 0, 2, 4)
		spr(304, 27, 68, 0, 1, 1, 0, 2, 4)
		spr(304, 167, 72, 0, 2, 1, 0, 2, 4)
		spr(304, 6, 81, 0, 2, 0, 0, 2, 4)
		
		-- Show Text
		print("VOGELJAGD", 66, 32, 1, false, 2)
		print("HIGHSCORE: " .. highscore, 84, 51, 1, false, 1)
		print("Press your MouseButton to begin!", 34, 63, 1, false, 1)
		print("Copyright (C) 2017", 73, 115, 15, false, 1)
		print("Niklas 'Nalquas' Freund", 58, 122, 15, false, 1)
		print("GNU General Public License Version 3", 19, 129, 15, false, 1)
		print("V"..versionNr, 2, 2, 15, false, 1)
		
		if starterStarted then t = t + 1 end
		if t > 7 then
		 started = true
		 t = 0
		end
		
	else
	 if t > 10 then t = 0 end
		if tsec > 59 and not gameOver then tsec = 0 end
		
		-- =======INPUT SECTION=======
		mx,my,md=mouse()
		
		-- =======GAME LOGIC SECTION=======
		if tsec == 59 and not gameOver then
	 	--sfx(0, "C-2", 8, 1, 5, 0)
			
			
		 remainingTime = remainingTime - 1
			if remainingTime % 10 == 0 then
			 sfx(0, "C-3", 8, 2, 14, 0)
			else
			 sfx(0, "A#2", 8, 2, 10, 0)
			end
		end
		
		if remainingTime == 0 and not gameOver then
		 gameOver = true
			shakeActive = false
			shake = -50
			tsec = 0
			music(0, -1, -1, false)
			if highscore < score then
			 pmem(0, score)
			end
		end
		
		if math.random() > 0.99 then
		 birds[#birds + 1] = MakeBird()
		end
		
		for cloudNr = 0, #clouds do
		 clouds[cloudNr].x = clouds[cloudNr].x + clouds[cloudNr].movement
			if clouds[cloudNr].movement > 0 and clouds[cloudNr].x > 240 then
			 clouds[cloudNr].x = -20
			elseif clouds[cloudNr].movement < 0 and clouds[cloudNr].x < -20 then
			 clouds[cloudNr].x = 240
			end
		end
		
		for birdNr = 0, #birds do
		 if birds[birdNr].alive then
			 if birds[birdNr].direction == 1 then
				 birds[birdNr].x = birds[birdNr].x + birds[birdNr].speed
				else
				 birds[birdNr].x = birds[birdNr].x - birds[birdNr].speed
				end
				
				if t == 10 then
				 birds[birdNr].state = birds[birdNr].state + 1
				end
				
				if birds[birdNr].state > 1 then
				 birds[birdNr].state = -1
				end
				
				if birds[birdNr].direction == 1 and birds[birdNr].x > 240 then
				 birds[birdNr].alive = false
				elseif birds[birdNr].direction == 2 and birds[birdNr].x < (-8)*birds[birdNr].layer then
				 birds[birdNr].alive = false
				end
			elseif birds[birdNr].deadTime < 300 then
			 birds[birdNr].y = birds[birdNr].y + birds[birdNr].speed
				if birds[birdNr].layer == 1 and birds[birdNr].y > 100 then
				 birds[birdNr].deadTime = 300
				elseif birds[birdNr].layer == 2 and birds[birdNr].y > 120 then
				 birds[birdNr].deadTime = 300
				elseif birds[birdNr].layer == 3 and birds[birdNr].y > 140 then
				 birds[birdNr].deadTime = 300
				end
				birds[birdNr].deadTime = birds[birdNr].deadTime + 0.25
			end
		end
		
		if not gameOver then
			if md and not shakeActive then
			 -- Shoot Gun
			 shakeActive = true
				shake = 10
				ammo = ammo - 1
				sfx(1, "B-3", 31, 0, 15, 0)
				for birdNr = 0, #birds do
				 if birds[birdNr].alive then
					 if mx >= birds[birdNr].x and my >= birds[birdNr].y then
							xAdd = 8 * birds[birdNr].layer
						 if mx <= birds[birdNr].x+xAdd and my <= birds[birdNr].y+xAdd then
								birds[birdNr].alive = false
								sfx(3, "A-4", 8, 1, 10, 0)
								if birds[birdNr].layer == 1 then
								 score = score + 3
								elseif birds[birdNr].layer == 2 then
								 score = score + 2
								else
									score = score + 1
								end
							end
						end
					end
				end
			end
			
			if shakeActive then
			 -- Decrease Shake after Shot
			 shake = shake - 1
				
				-- Reloading-Sound
				if ammo > 0 then
					if shake == -20 then
					 sfx(2, "D-3", 8, 1, 10, 0)
					elseif shake == -40 then
					 sfx(2, "D#3", 8, 1, 10, 0)
					end
			 else
				 if shake == -30 then
					 sfx(2, "D-3", 8, 1, 10, 0)
					elseif shake == -80 then
					 sfx(2, "D#3", 8, 1, 10, 0)
					elseif shake == -110 then
					 sfx(2, "G#3", 8, 1, 10, 0)
					end
				end
				
				-- Reactivate Gun
				if ammo > 0 then
					if shake < -50 then
					 shake = -50
						shakeActive = false
					end
				else
				 if shake < -120 then
					 ammo = 5
					 shake = -50
						shakeActive = false
					end
				end
			end
		end
		
		-- =======RENDER SECTION=======
		-- Clear Screen and display background
		cls(12)
		map(0, 0, 30, 17, 0, 0, -1, 1)
		
		-- Show Clouds
		for cloudNr = 0, #clouds do
		 spr(clouds[cloudNr].sprite, clouds[cloudNr].x, clouds[cloudNr].y, 0, 2, 0, 0, 1, 1)
		end
		
		-- Show Trees in Background
		spr(304, 223, 65, 0, 1, 0, 0, 2, 4)
		spr(304, 27, 68, 0, 1, 1, 0, 2, 4)
		
		-- Show Birds
		for currentLayer = 1, 3 do
			for birdNr = 0, #birds do
			 if birds[birdNr].layer == currentLayer then
				 if birds[birdNr].alive then
					 faceDirection = birds[birdNr].direction - 1
					 spr(birds[birdNr].birdSprite+birds[birdNr].state, birds[birdNr].x, birds[birdNr].y, 15, birds[birdNr].layer, faceDirection, 0, 1, 1)
					elseif birds[birdNr].deadTime < 300 then
					 faceDirection = birds[birdNr].direction - 1
					 spr(birds[birdNr].birdSprite+birds[birdNr].state, birds[birdNr].x, birds[birdNr].y, 15, birds[birdNr].layer, faceDirection, 1, 1, 1)
					end
				end
			end
		end
		
		-- Show Trees in Foreground
		spr(304, 167, 72, 0, 2, 1, 0, 2, 4)
		spr(304, 6, 81, 0, 2, 0, 0, 2, 4)
		
		-- Show remaining Ammo
		for am = 1, ammo do
			spr(318, 237-(am*16), 118, 0, 2, 0, 0, 1, 1)
		end
		if ammo < 5 then
		 for am = ammo+1, 5 do
				spr(319, 237-(am*16), 118, 0, 2, 0, 0, 1, 1)
			end
		end
		
		-- Show Targeting Cross
		circb(mx, my, 3, 6)
		circ(mx, my, 1, 6)
		
		-- Show Score
	 print("HIGHSCORE: " .. highscore, 2, 2, 1)
		print("SCORE: " .. score, 2, 10, 1)
		print("TIME LEFT: " .. remainingTime, 2, 18, 1)
		if gameOver then
		 print("GAME OVER!", 68, 58, 1, false, 2)
			print("SCORE: " .. score, 100, 70, 1, false, 1)
		end
		
		-- Show Gun
		if mx <= 86 then
		 if shake > 0 then
			 spr(286, 100, 96, 0, 5, 0, 0, 1, 1)
			elseif shake > -10 then
			 spr(302, 100, 96, 0, 5, 0, 0, 1, 1)
			else
			 spr(270, 100, 96, 0, 5, 0, 0, 1, 1)
			end
		elseif mx <= 173 then
			if shake > 0 then
			 spr(285, 100, 96, 0, 5, 0, 0, 1, 1)
			elseif shake > -10 then
			 spr(301, 100, 96, 0, 5, 0, 0, 1, 1)
			else
			 spr(269, 100, 96, 0, 5, 0, 0, 1, 1)
			end
		else
			if shake > 0 then
			 spr(287, 100, 96, 0, 5, 0, 0, 1, 1)
			elseif shake > -10 then
			 spr(303, 100, 96, 0, 5, 0, 0, 1, 1)
			else
			 spr(271, 100, 96, 0, 5, 0, 0, 1, 1)
			end
		end
		
		-- =======DEBUG SECTION=======
		--print(mx .. ":" .. my, 84, 64, 9)
		--if md then print("PRESSED", 84, 70, 9) end
		--print(birds[0].direction ..":".. birds[0].layer ..":".. birds[0].state ..":".. birds[0].x ..":".. birds[0].y ..":".. birds[0].speed, 10, 80)
		--print(birds.amount, 90, 90)
		--print(ammo, 90, 60)
		
		t = t + 1
		tsec = tsec + 1
		
		-- Reset Game
		if gameOver and tsec > 359 then
		 ResetValues()
		end
	end
end

function scanline(row)
	if shake>=0 and shakeActive then
	 poke(0x3FF9,math.random(-shake,shake)) -- Horizontal Shake
		poke(0x3FFA,math.random(-shake,shake)) -- Vertical Shake
	else
	 poke(0x3FF9,0)
		poke(0x3FFA,0)
	end
end
